DATA SEGMENT
	BCD_INPUT DB 17H
	BIN_VALUE DB ?
DATA ENDS

STACK_SEG SEGMENT
	DW 40 DUP(0)
	TOP_STACK LABEL WORD
STACK_SEG ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA,SS:STACK_SEG
START:	MOV AX,DATA
		MOV DS,AX
		MOV AX,STACK_SEG
		MOV SS,AX
		MOV SP,offset TOP_STACK
		MOV AL,BCD_INPUT
		CALL BCD_TO_BIN
		MOV BIN_VALUE,AL
		INT 3h
		
BCD_TO_BIN PROC NEAR
		PUSHF
		PUSH BX
		PUSH CX
		MOV BL,AL
		AND BL,0FH
		AND AL,0f0H
		MOV CL,04H
		SHR Al,CL
		MOV BH,0AH
		MUL BH
		ADD AL,BL
		
		POP CX
		POP BX
		POPF
		RET
		
BCD_TO_BIN ENDP
CODE ENDS
END START
		